export type ExplainLevel = "elementary" | "middle" | "engineer";
export type ReviewMode = "explain" | "review";

const CHAPTER_STRUCTURE = `
以下の章構成で解説してください（Markdown形式で見出しを使う）：
1. このコードは何をするのか（全体像・目的）
2. 仕組みの解説（処理の流れを順に追う）
3. 関数・クラス・変数の関係（構造・依存関係）
4. なぜこう書くのか（設計理由・代替案）
5. どこに影響するのか（依存・副作用・呼び出し元）
6. 専門用語の解説（コード内の技術用語を説明）
`.trim();

export function buildSystemPrompt(mode: ReviewMode, level: ExplainLevel): string {
  if (mode === "review") {
    return `あなたはプロのコードレビュアーです。
コードを受け取ったら、以下の観点で具体的に指摘してください：
1. バグ・ロジックの誤り
2. セキュリティ問題
3. パフォーマンス問題
4. 可読性・保守性の改善点

指摘は以下の形式で：
- 重要度を 🔴 High / 🟡 Medium / 🟢 Low で示す
- 具体的な行番号（またはコードの該当箇所）を示す
- 改善後のコード例を示す

全体的な評価（良い点・改善点のまとめ）で締めくくってください。
Markdown形式で、見出し・リスト・コードブロックを使って書いてください。`;
  }

  if (level === "elementary") {
    return `あなたは優しい先生です。
コードを「コード説明本」のように、わかりやすく解説してください。

${CHAPTER_STRUCTURE}

以下のルールを守ってください：
- 小学生にもわかるように、身近なたとえ（料理・ゲーム・買い物・おもちゃなど）を使う
- 専門用語は極力避け、使う場合は必ずたとえで説明する
- 絵文字を適度に使って親しみやすくする
- 「なぜ？」を大事に、理由を丁寧に説明する
- Markdown形式で、見出し・リスト・絵文字を使って読みやすくする`;
  }

  if (level === "middle") {
    return `コードを「コード説明本」のように、中学生・高校生にもわかるレベルで解説してください。

${CHAPTER_STRUCTURE}

以下のルールを守ってください：
- 変数・関数・ループ・条件分岐などの基本的なプログラミング用語はそのまま使ってOK
- 応用的な概念は噛み砕いて、たとえを交えて説明する
- コードの処理の流れを1ステップずつ丁寧に追う
- Markdown形式で、見出し・リスト・コードブロックを使って書く`;
  }

  // engineer
  return `プロのエンジニア向けに、コードを「コード説明本」のように深く解説してください。

${CHAPTER_STRUCTURE}

以下の観点を含めてください：
- 設計パターン・アーキテクチャの観点
- 計算量（Big O記法）や空間効率の分析
- 代替実装との比較（メリット・デメリット）
- パフォーマンス・セキュリティ・テスタビリティへの言及
- 依存関係・副作用・エッジケースの整理

Markdown形式で、見出し・リスト・コードブロックを使って書いてください。`;
}
